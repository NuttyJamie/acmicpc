#include <stdio.h>

int main()
{
	int n, i, t=1;

	scanf("%d", &n);

	if(n != 1)
	{
		for(i=1; i < n; i<<=1);
		i>>=1;
		t=0;
		while(i++ < n)	t+=2;
	}
	
	printf("%d", t);
}


